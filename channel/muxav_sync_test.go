// offMsec is the maximum absolute difference (in milliseconds) between audio
// and video start_time in the muxed output that we consider "in sync".
// ffprobe reports start_time with floating-point precision, and the mp4
// timescale granularity means sub-frame differences are inevitable.  50ms
// matches the threshold used by the MuxAV probe-based alignment code.
const syncThresholdMsec = 50.0

// streamStart contains ffprobe start_time info for one stream.
type streamStart struct {
	CodecType string  `json:"codec_type"`
	StartTime float64 `json:"start_time,string"`
}

// probeStartTimes returns the start_time (seconds) of the first video and
// first audio stream in path.  Returns -1 for a stream not found.
//
// Uses CombinedOutput and includes stderr in returned error for debugging.
func probeStartTimes(path string) (videoStart, audioStart float64, err error) {
	ctx, cancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer cancel()
	cmd := config.FFprobeCommandContext(ctx,
		"-v", "error",
		"-show_entries", "stream=codec_type,start_time",
		"-of", "json",
		path,
	)
	out, err := cmd.CombinedOutput()
	if err != nil {
		return 0, 0, fmt.Errorf("ffprobe: %w\n%s", err, string(out))
	}
	var p struct {
		Streams []streamStart `json:"streams"`
	}
	if err := json.Unmarshal(out, &p); err != nil {
		return 0, 0, fmt.Errorf("parse ffprobe: %w\n%s", err, string(out))
	}
	videoStart, audioStart = -1, -1
	for _, s := range p.Streams {
		switch s.CodecType {
		case "video":
			videoStart = s.StartTime
		case "audio":
			audioStart = s.StartTime
		}
	}
	return videoStart, audioStart, nil
}

// probeDuration returns the container-level duration (seconds) of a media file.
func probeDuration(path string) (float64, error) {
	ctx, cancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer cancel()
	out, err := config.FFprobeCommandContext(ctx,
		"-v", "error",
		"-show_entries", "format=duration",
		"-of", "default=noprint_wrappers=1:nokey=1",
		path,
	).Output()
	if err != nil {
		return 0, err
	}
	return strconv.ParseFloat(strings.TrimSpace(string(out)), 64)
}

// requireFFmpeg skips t if ffmpeg or ffprobe is not available in PATH.
func requireFFmpeg(t *testing.T) {
	t.Helper()
	if err := config.ValidateFFmpeg(); err != nil {
		t.Skipf("ffmpeg/ffprobe not available: %v", err)
	}
}

// fMP4Pair holds paths to a pair of fragmented MP4 files (video-only,
// audio-only) generated by generateFMP4Pair.
type fMP4Pair struct {
	Video    string
	Audio    string
	Duration int // seconds
}

// generateFMP4Pair creates video-only and audio-only fragmented MP4 files.
// The audio track is shifted by audioDelaySec so it starts that many seconds
// after the video.  Both files are placed inside dir.
//
// The caller must clean up dir (typically via t.TempDir()).
func generateFMP4Pair(t *testing.T, dir string, durationSec int, audioDelaySec float64) fMP4Pair {
	t.Helper()

	// ── Video: colour bars, 30fps, libx264, no audio ──────────────────
	videoPath := filepath.Join(dir, "src-video.mp4")
	cmd := exec.Command("ffmpeg",
		"-y",
		"-f", "lavfi",
		"-i", fmt.Sprintf("testsrc2=d=%d:s=640x480:r=30", durationSec),
		"-c:v", "libx264",
		"-preset", "ultrafast",
		"-crf", "28",
		"-an",
		"-movflags", "+frag_keyframe+empty_moov",
		videoPath,
	)
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("generate video fMP4: %v\n%s", err, out)
	}

	// ── Audio: clean sine tone, AAC, no video ─────────────────────────
	cleanAudio := filepath.Join(dir, "src-audio-clean.mp4")
	cmd = exec.Command("ffmpeg",
		"-y",
		"-f", "lavfi",
		"-i", fmt.Sprintf("sine=frequency=440:d=%d", durationSec),
		"-c:a", "aac",
		"-b:a", "128k",
		"-vn",
		"-movflags", "+frag_keyframe+empty_moov",
		cleanAudio,
	)
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("generate audio fMP4: %v\n%s", err, out)
	}

	// ── Shift audio timestamps by audioDelaySec ───────────────────────
	// A positive delay pushes audio forward, causing it to start after
	// video (the most common real-world scenario for LL-HLS).
	//
	// We re-encode (-c:a aac) rather than stream-copy because ffmpeg's
	// -itsoffset applied at the demuxer level shifts raw output timestamps
	// properly only via re-encode (stream copy leaves fMP4 tfdt boxes
	// unchanged in a way that confuses ffprobe's stream-level probes).
	audioPath := filepath.Join(dir, "src-audio.mp4")
	if math.Abs(audioDelaySec) < 0.001 {
		audioPath = cleanAudio
	} else {
		cmd = exec.Command("ffmpeg",
			"-y",
			"-itsoffset", fmt.Sprintf("%f", audioDelaySec),
			"-i", cleanAudio,
			"-c:a", "aac",
			"-b:a", "128k",
			"-movflags", "+frag_keyframe+empty_moov",
			audioPath,
		)
		if out, err := cmd.CombinedOutput(); err != nil {
			t.Fatalf("shift audio timestamps: %v\n%s", err, out)
		}
	}

	return fMP4Pair{
		Video:    videoPath,
		Audio:    audioPath,
		Duration: durationSec,
	}
}

// verifySync probes the muxed output and fails t if the audio and video
// start times differ by more than syncThresholdMsec.
func verifySync(t *testing.T, muxedPath string) {
	t.Helper()

	vStart, aStart, err := probeStartTimes(muxedPath)
	if err != nil {
		t.Fatalf("probe muxed output: %v", err)
	}
	if vStart < 0 {
		t.Fatal("muxed output has no video stream")
	}
	if aStart < 0 {
		t.Fatal("muxed output has no audio stream")
	}

	diff := math.Abs(aStart - vStart) * 1000 // convert to msec
	if diff > syncThresholdMsec {
		t.Fatalf("A/V sync mismatch: video=%.3fs audio=%.3fs diff=%.1fms (threshold=%.0fms)",
			vStart, aStart, diff, syncThresholdMsec)
	}
	t.Logf("A/V sync OK: video=%.3fs audio=%.3fs diff=%.1fms", vStart, aStart, diff)
}

